#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	if (!s1)
		return (0);
	while (*s1)
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return (1);
}
